// заполнение рисунка
void FillImage(int row, int col)
{
    if (row >= 0 && row < pic.GetLength(0) && col >= 0 && col < pic.GetLength(1))
    {
        if (pic[row, col] == 0)
        {
            pic[row, col] = 1;
            FillImage(row - 1, col);
            FillImage(row, col - 1);
            FillImage(row + 1, col);
            FillImage(row, col + 1);
        }
    }
}

// печать массива
void PrintPicture(int[,] table)
{
    for (int row = 0; row < table.GetLength(0); row++)
    {
        for (int columns = 0; columns < table.GetLength(1); columns++)
        {
            if (table[row, columns] == 1)
            {
                Console.Write("*");
            }
            else
            {
                Console.Write(" ");
            }

        }
        Console.WriteLine();
    }
}